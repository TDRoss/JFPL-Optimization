using CSV, DataFrames, JuMP, SCIP, PlutoUI

# ╔═╡ cb6f315b-997f-4142-8085-1f84067ef540
df = CSV.read("../data/input.csv",DataFrame);

# ╔═╡ ccebdf08-f983-4af4-a2b4-6e8e4bdd2376
gk_data = subset(df, :Pos => ByRow(pos -> pos == "G"))

# ╔═╡ 46cff79a-cb49-497c-ba6c-453e3aa52036
numplayers = length(gk_data[:,:ID])

# ╔═╡ 9f0b1ced-b168-4ac2-966e-8577c992ca19
points = gk_data[:,"1_Pts"]

# ╔═╡ e6f2178f-cd6a-4022-a581-7c11deef44fe
price = gk_data[:,:BV]

# ╔═╡ bde745a1-63e3-4236-92c7-e8b4d41a56a3
budget = 9.6;

# ╔═╡ b098d41a-3f8e-4ed1-a2d2-09b8cb1f8a0f
begin #Begin statement is just so all the model parameters are in one cell in Pluto
	model = Model(SCIP.Optimizer)
	@variable(model, lineup[1:numplayers], Bin);
	@variable(model, bench[1:numplayers], Bin);
	total_xp = sum(lineup .* points) + 0.1 * sum(bench .* points);
	@objective(model, Max, total_xp);
	@constraint(model, lineup + bench .<= 1);
	@constraint(model, sum(lineup) == 1);
	@constraint(model, sum(bench) == 1);
	@constraint(model, sum((lineup+bench).*price) <= budget);
end

# ╔═╡ 04258c9b-c5f1-426e-a12b-7c9cd4768709
optimize!(model)

# ╔═╡ d497a0d5-06c7-4c53-8e3b-85666bec5944
begin
	with_terminal() do #This is just a way to get Pluto to print out the results
	print("LINEUP\n")
	for p in 1:numplayers
	    if value(lineup[p]) > 0.5
	        print(gk_data[p,:])
		end
	end
	print("\nBENCH\n")
	for p in 1:numplayers
    	if value(bench[p]) > 0.5
        	print(gk_data[p,:])
		end
	end
	end
end

